        var nvm= new Vue({
            el: '#app2',
            data: {
                loading: false,
                historyCart: [],
                priceList:[],
                checkout: false,
                selected:[],
                total: null,
                costumer: {
                    name: null,
                    email: null,
                    address: null,
                    state: null,
                    country: null,
                    postCode: null,
                    remark: null
                },
                errors:[]
            },
            created() {
                this.getLsCart()
            },
            methods: {
                getLsCart: function(){
                    if(localStorage.hasOwnProperty('bkCart')){
                        this.historyCart = JSON.parse(localStorage.getItem('bkCart'))
                    }
                },
                checkoutNow:function(){
                    axios.get('{{ url('photo-price') }}').then(response=>{
                        this.priceList = response.data
                        this.checkout = true;
                    }).catch(err=>{
                        alert('Found something error')
                    })
                },
                deleteCart: function(index){
                    this.historyCart.splice(index,1)
                    localStorage.setItem('bkCart',JSON.stringify(this.historyCart))
                },
                checkTotal:function(){
                    const panjang = this.selected
                    var total = 0;
                    for (let index = 0; index < panjang.length; index++) {
                       const el = panjang[index].price.value
                       const harga = el.split(',')
                       total +=parseInt(harga[1])
                        
                    }
                    this.total = total
                },
                makeOrder: function(){
                    if(this.validate() && this.selected.length>0){
                        this.loading = true
                        var data = new FormData
                        data.append('selected',JSON.stringify(this.selected))
                        data.append('costumer',JSON.stringify(this.costumer))
                        data.append('_token','{{ csrf_token() }}')
                        axios.post('{{ route('sales.store') }}',data).then(response=>{
                            /* remove local storage */
                            localStorage.removeItem('bkCart')
                            location.reload()
                            alert('We have receiver your order our tema will be contac you sortly')
                        }).catch(err=>{
                            alert('Found something wrong')
                        })
                    }
                   
                },
                validate: function(){
                    const errorsContainer = []
                    if (this.costumer.name &&this.costumer.email&& this.costumer.address && this.costumer.state && this.costumer.country && this.costumer.postCode) {
                        if (!this.validEmail(this.costumer.email)) {
                        errorsContainer.push('Email inValid format')
                        this.errors = errorsContainer
                        return false
                        }
                        else{
                            return true
                        }
                        
                    } else {
                        if (!this.costumer.name) {
                        errorsContainer.push('Name Required')
                        }
                        if (!this.costumer.address) {
                        errorsContainer.push('Address Required')
                        }
                        if (!this.costumer.state) {
                        errorsContainer.push('State Required')
                        }
                        if (!this.costumer.country) {
                        errorsContainer.push('Country Required')
                        }
                        if (!this.costumer.postCode) {
                        errorsContainer.push('postCode Required')
                        }
                        if (!this.costumer.email) {
                        errorsContainer.push(' Email Required')
                        } else if (!this.validEmail(this.costumer.email)) {
                        errorsContainer.push('Email inValid format')
                        }
                        this.errors = errorsContainer
                        return false
                    }
                },
                validEmail: function (email) {
                    var re = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
                    return re.test(email)
                },
            }, 
        })
